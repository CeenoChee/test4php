function slugify($str): string
{
    return strtolower(preg_replace(['/[^a-zA-Z0-9 -]/', '/[ -]+/', '/^-|-$/'], ['', '-', ''], remove_accent($str)));
}

function permission($roles)
{
    $user = Auth::user();

    return $user->getPermission($roles);
}

function sql($query): string
{
    $bindings = array_map(function ($binding) {
        return is_numeric($binding) ? $binding : "'{$binding}'";
    }, $query->getBindings());

    $sql = preg_replace('/[ \t]+/', ' ', str_replace(PHP_EOL, ' ', $query->toSql()));

    return Str::replaceArray('?', $bindings, $sql) . ';';
}

function calculateReadingTime(string $text)
{
    $length = mb_strlen(str_replace('&nbsp;', '', $text));
    $minutes = ($length / 15) / 60;  // 15 karakter + képek = ~1mp

    if ($minutes <= 1) {
        return 1 . ' ' . trans('dates.minute');
    }

    return round($minutes) . ' ' . trans('dates.minutes');
}

function isDecimal($val)
{
    return is_numeric($val) && floor($val) != $val;
}

